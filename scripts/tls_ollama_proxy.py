#!/usr/bin/env python3
"""TLS-terminating reverse proxy in front of local Ollama.

MARBLE's OpenAI-compatible client talks to this proxy over HTTPS; the proxy
forwards plaintext to the real Ollama server on 127.0.0.1:11434. This makes
every byte MARBLE <-> proxy genuinely TLS-encrypted on the wire, so a
passive tcpdump capture on the proxy's port sees only ciphertext -- no
prompts, no completions, no model output -- exactly the website-fingerprinting
threat model, applied to real agent-to-model inference traffic instead of
web browsing.

Uses a self-signed cert generated by scripts/generate_certs.sh into
MARBLE/certs/{cert,key}.pem.
"""

from __future__ import annotations

import argparse
import socket
import ssl
import threading
from pathlib import Path

DEFAULT_CERT = Path(__file__).resolve().parent.parent / "certs" / "cert.pem"
DEFAULT_KEY = Path(__file__).resolve().parent.parent / "certs" / "key.pem"


def relay(src: socket.socket, dst: socket.socket) -> None:
    try:
        while True:
            data = src.recv(65536)
            if not data:
                break
            dst.sendall(data)
    except OSError:
        pass
    finally:
        try:
            dst.shutdown(socket.SHUT_WR)
        except OSError:
            pass


def handle_client(tls_conn: ssl.SSLSocket, upstream_host: str, upstream_port: int) -> None:
    try:
        upstream = socket.create_connection((upstream_host, upstream_port), timeout=120)
    except OSError:
        tls_conn.close()
        return
    t1 = threading.Thread(target=relay, args=(tls_conn, upstream), daemon=True)
    t2 = threading.Thread(target=relay, args=(upstream, tls_conn), daemon=True)
    t1.start()
    t2.start()
    t1.join()
    t2.join()
    tls_conn.close()
    upstream.close()


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--listen-port", type=int, default=11443)
    parser.add_argument("--upstream-host", default="127.0.0.1")
    parser.add_argument("--upstream-port", type=int, default=11434)
    parser.add_argument("--cert", default=str(DEFAULT_CERT))
    parser.add_argument("--key", default=str(DEFAULT_KEY))
    args = parser.parse_args()

    context = ssl.SSLContext(ssl.PROTOCOL_TLS_SERVER)
    context.load_cert_chain(certfile=args.cert, keyfile=args.key)

    listener = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    listener.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
    listener.bind(("127.0.0.1", args.listen_port))
    listener.listen(64)
    print(f"TLS proxy listening on 127.0.0.1:{args.listen_port} -> {args.upstream_host}:{args.upstream_port}")

    while True:
        raw_conn, addr = listener.accept()
        try:
            tls_conn = context.wrap_socket(raw_conn, server_side=True)
        except ssl.SSLError as e:
            print(f"TLS handshake failed from {addr}: {e}")
            raw_conn.close()
            continue
        threading.Thread(target=handle_client, args=(tls_conn, args.upstream_host, args.upstream_port), daemon=True).start()


if __name__ == "__main__":
    main()
